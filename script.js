const milestoneList = document.querySelector("#milestoneList");
const referenceList = document.querySelector("#referenceList");
const modal = document.querySelector("#storyModal");
const modalClose = document.querySelector("#modalClose");
const themeToggle = document.querySelector("#themeToggle");
let lastTrigger = null;

function renderMilestones() {
  milestoneList.innerHTML = milestones
    .map(
      (milestone, index) => `
        <article class="milestone-row" id="${milestone.id}">
          <div class="milestone-inner">
            <div class="milestone-copy">
              <div class="milestone-number" aria-hidden="true">
                <span>${index + 1}</span>
              </div>
              <div>
                <p class="milestone-date">${milestone.date}</p>
                <h2>${milestone.title}</h2>
                <p class="milestone-summary">${milestone.summary}</p>
                <button
                  class="story-button"
                  type="button"
                  data-milestone="${milestone.id}"
                  aria-haspopup="dialog"
                >
                  Leer historia completa
                </button>
              </div>
            </div>
            <figure class="milestone-media">
              <img
                src="${milestone.image}"
                alt="${milestone.alt}"
                loading="${index === 0 ? "eager" : "lazy"}"
                decoding="async"
              >
            </figure>
          </div>
        </article>
      `
    )
    .join("");
}

function renderReferences() {
  referenceList.innerHTML = references
    .map(
      (reference) => `
        <li>
          ${reference.author} (${reference.date}).
          <cite>${reference.title}</cite>${reference.publication ? `. ${reference.publication}` : ""}.
          <a href="${reference.url}" target="_blank" rel="noopener noreferrer">${reference.url}</a>
        </li>
      `
    )
    .join("");
}

function openStory(id, trigger) {
  const milestone = milestones.find((item) => item.id === id);
  if (!milestone) return;

  lastTrigger = trigger;
  document.querySelector("#modalDate").textContent = milestone.date;
  document.querySelector("#modalTitle").textContent = milestone.title;
  document.querySelector("#modalDescription").textContent = milestone.description;
  document.querySelector("#modalImage").src = milestone.image;
  document.querySelector("#modalImage").alt = milestone.alt;
  document.querySelector("#modalSource").href = milestone.source;
  document.body.classList.add("modal-open");
  modal.showModal();
}

function closeStory() {
  if (!modal.open) return;
  modal.close();
  document.body.classList.remove("modal-open");
  lastTrigger?.focus();
}

function applyTheme(theme) {
  document.documentElement.dataset.theme = theme;
  const isDark = theme === "dark";
  themeToggle.setAttribute("aria-pressed", String(isDark));
  themeToggle.setAttribute("aria-label", isDark ? "Activar modo claro" : "Activar modo oscuro");
  themeToggle.querySelector(".theme-label").textContent = isDark ? "Modo claro" : "Modo oscuro";
  localStorage.setItem("viking-theme", theme);
}

function initializeTheme() {
  const savedTheme = localStorage.getItem("viking-theme");
  const preferredTheme = window.matchMedia("(prefers-color-scheme: dark)").matches ? "dark" : "light";
  applyTheme(savedTheme || preferredTheme);
}

renderMilestones();
renderReferences();
initializeTheme();

milestoneList.addEventListener("click", (event) => {
  const trigger = event.target.closest("[data-milestone]");
  if (trigger) openStory(trigger.dataset.milestone, trigger);
});

themeToggle.addEventListener("click", () => {
  const nextTheme = document.documentElement.dataset.theme === "dark" ? "light" : "dark";
  applyTheme(nextTheme);
});

modalClose.addEventListener("click", closeStory);

modal.addEventListener("click", (event) => {
  if (event.target === modal) closeStory();
});

modal.addEventListener("close", () => {
  document.body.classList.remove("modal-open");
});
